#!/usr/bin/env python3
"""Patch L - Decode percent-escaped Node package folders in plugins.

Some upstream/rebuild zips can carry scoped npm package folders with the scope
escaped as `%40`, for example:

    resources/plugins/openai-bundled/plugins/computer-use/node_modules/%40oai

The Computer Use plugin imports `../node_modules/@oai/sky/...` and its compiled
Sky bundle imports pnpm-style paths such as `.pnpm/@rollup_...@...`. Node cannot
resolve those vendored packages when the zip has percent-escaped folder names.
This patch renames percent-escaped package folders such as `%40oai`,
`%40rollup_plugin-typescript%401_...`, and `objc-js%401.5.0` back to their
decoded names, then verifies `@oai/sky` exists when the Computer Use plugin is
present.

Idempotent: rerunning after the rename is a no-op.
"""
import argparse
import json
import urllib.parse
from pathlib import Path


def decode_package_dir_name(name: str) -> str:
    decoded = urllib.parse.unquote(name)
    if "%40" in name and decoded != name:
        return decoded
    return ""


def rewrite_lockfile_paths(node_modules_dir: Path):
    changed = []
    candidates = [
        node_modules_dir / ".package-lock.json",
        node_modules_dir.parent / "package-lock.json",
    ]
    for path in candidates:
        if not path.exists() or not path.is_file():
            continue
        text = path.read_text(encoding="utf-8")
        updated = text.replace("node_modules/%40", "node_modules/@")
        if updated != text:
            path.write_text(updated, encoding="utf-8")
            changed.append(str(path))
    return changed


def patch_node_modules(node_modules_dir: Path):
    renamed = []
    lockfiles = []

    try:
        children = list(node_modules_dir.iterdir())
    except FileNotFoundError:
        return renamed, lockfiles

    for child in children:
        if not child.is_dir() or "%40" not in child.name:
            continue

        decoded = decode_package_dir_name(child.name)
        if not decoded:
            continue

        target = child.with_name(decoded)
        if target.exists():
            raise SystemExit(
                f"Cannot rename {child} to {target}: target already exists. "
                "Inspect the package tree before releasing."
            )

        child.rename(target)
        renamed.append({"from": str(child), "to": str(target)})

    if renamed:
        lockfiles.extend(rewrite_lockfile_paths(node_modules_dir))

    return renamed, lockfiles


def find_package_container_dirs(plugins_root: Path):
    dirs = []
    for path in plugins_root.rglob("*"):
        try:
            if not path.is_dir() or path.name not in {"node_modules", ".pnpm"}:
                continue
        except FileNotFoundError:
            continue

        dirs.append(path)
    return dirs


def verify_computer_use_plugin(app_dir: Path):
    plugin = (
        app_dir
        / "resources"
        / "plugins"
        / "openai-bundled"
        / "plugins"
        / "computer-use"
    )
    if not plugin.exists():
        return {"present": False, "status": "skipped"}

    node_modules = plugin / "node_modules"
    if not node_modules.exists():
        raise SystemExit(f"Computer Use plugin is missing node_modules: {node_modules}")

    escaped = [
        str(p.relative_to(plugin))
        for p in plugin.rglob("*")
        if p.is_dir() and "%40" in p.name
    ]
    if escaped:
        raise SystemExit(
            "Computer Use plugin still has percent-escaped package folders: "
            + ", ".join(escaped)
        )

    sky_pkg = node_modules / "@oai" / "sky" / "package.json"
    if not sky_pkg.exists():
        raise SystemExit(f"Computer Use plugin cannot resolve @oai/sky: missing {sky_pkg}")

    return {"present": True, "status": "ok", "sky_package": str(sky_pkg)}


def main():
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "--app-dir",
        required=True,
        help="Codex install dir (contains resources/plugins)",
    )
    args = parser.parse_args()

    app_dir = Path(args.app_dir).resolve()
    plugins_root = app_dir / "resources" / "plugins"
    if not plugins_root.exists():
        print(json.dumps({
            "status": "skipped",
            "reason": "resources/plugins not present",
            "app_dir": str(app_dir),
        }, indent=2))
        return

    renamed = []
    lockfiles = []
    for node_modules in find_package_container_dirs(plugins_root):
        r, locks = patch_node_modules(node_modules)
        renamed.extend(r)
        lockfiles.extend(locks)

    computer_use = verify_computer_use_plugin(app_dir)

    print(json.dumps({
        "status": "patched" if renamed or lockfiles else "already_patched",
        "app_dir": str(app_dir),
        "renamed": renamed,
        "lockfiles_updated": lockfiles,
        "computer_use": computer_use,
    }, indent=2))


if __name__ == "__main__":
    main()
